def get_grade (num)
	
	if num >=90
		then return "A"

	elsif num >=80 && num <90
		then return "B"

	elsif num >=70 && num <80
		then return "C"

	elsif num >=60 && num <70
		then return "D"

	else
	 	return "F"	
	end
end
